# Convert an integer to a float
int_value = 42
float_from_int = float(int_value) # Output: 42.0

# Convert a numeric string to a float
string_value = "3.14159"
float_from_string = float(string_value) # Output: 3.14159

# Convert the result of a division operation (standard division in Python 3 always returns a float)
result_of_division = 8 / 5 # Output: 1.6

# Convert special strings representing infinity or "Not a Number"
infinity = float("inf")
not_a_number = float("nan") 
